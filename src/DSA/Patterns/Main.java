package DSA.Patterns;

import java.awt.*;

public class Main {
    public static void main(String[] args) {
        //Basic of Patterns in java
        for(int i=0;i<10;i++){
            for(int j=0;j<i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }

}
