public class final1 {
    public static void main(String[] args) {
        // Final Keyword
        final double pi;
        // final keyword value cannot be changed once the value is initialized on the variable.
        pi=3.14159;
        final String name;
        name="Prabesh Bista";
        System.out.println(pi);
        System.out.println("My name is "+name);
    }
    
}
